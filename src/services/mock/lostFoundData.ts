export function computeLostFoundOverview(): LostFoundOverview {
  return {
    totalReports: mockLostItemReports.length,
    pendingDriverReview: mockLostItemReports.filter((r) => r.status === 'pending_review').length,
    itemsFound: mockLostItemReports.filter((r) => r.status === 'found').length,
    itemsNotFound: mockLostItemReports.filter((r) => r.status === 'not_found').length,
    pickupScheduled: mockLostItemReports.filter((r) => r.status === 'pickup_scheduled').length,
    deliveryScheduled: mockLostItemReports.filter((r) => r.status === 'delivery_scheduled').length,
    completedReturns: mockLostItemReports.filter((r) => r.status === 'completed').length,
    openDisputes: mockLostFoundDisputes.filter((d) => d.status === 'open' || d.status === 'escalated').length,
  }
}

